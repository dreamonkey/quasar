<template>
  <q-form
    v-if="showSearchInputField || isOpenByDefault"
    autocapitalize="off"
    autocomplete="off"
    spellcheck="false"
    class="search-form position-relative"
  >
    <transition
      appear
      enter-active-class="animated fadeIn"
      leave-active-class="animated fadeOut"
    >
      <q-input
        ref="searchInputRef"
        v-model="searchTerms"
        :dark="!dark"
        :bg-color="dark? 'black-12':'white-28'"
        :input-class="`q-ml-xs text-size-16 letter-spacing-40 ${dark? 'text-black':'text-white'}`"
        :autofocus="$q.screen.lt.lg"
        standout
        dense
        debounce="300"
        hide-bottom-space
        placeholder="Search Quasar v2..."
        @keydown="onSearchKeydown"
        @focus="onSearchFocus"
      >
        <template #append>
          <q-icon
            v-if="!searchTerms"
            name="search"
            size="sm"
            color="lp-primary"
          />
          <q-icon
            v-else
            name="close"
            color="lp-primary"
            class="cursor-pointer"
            @click.stop="resetSearch"
          />
        </template>
      </q-input>
    </transition>
    <div class="search-result-field rounded-borders letter-spacing-25">
      <q-scroll-area
        :class="searchResults !== null? 'set-scrollarea-height':'scroll-area-opacity-0'"
        class="bg-white text-dark rounded-borders search-result-container shadow-bottom-medium"
      >
        <template v-if="searchResults !== null">
          <component
            v-if="searchResults.masterComponent !== void 0"
            :is="searchResults.masterComponent"/>
          <app-search-results
            v-else
            :results="searchResults"
            :search-has-focus="searchHasFocus"
            :search-active-id="searchActiveId"/>
        </template>
      </q-scroll-area>
    </div>
  </q-form>
  <q-btn
    v-else
    flat
    round
    color="lp-primary"
    icon="search"
    size="12px"
  />
</template>

<script>
import AppSearchResults from 'components/AppSearchResults.vue'
import useSearch from 'layouts/doc-layout/use-search'
import { useQuasar } from 'quasar'
import { useRoute } from 'vue-router'
import { watch } from 'vue'

export default {
  name: 'search-quasar-form',
  components: { AppSearchResults },
  props: {
    dark: {
      type: Boolean,
      default: true
    },
    showSearchInputField: {
      type: Boolean,
      default: false
    },
    isOpenByDefault: {
      type: Boolean,
      default: true
    }
  },
  setup (props) {
    const $q = useQuasar()
    const $route = useRoute()

    const scope = {}
    useSearch(scope, $q, $route)

    watch(() => props.showSearchInputField, () => {
      if (!props.showSearchInputField) {
        scope.onSearchBlur()
        scope.resetSearch()
      }
    })

    return scope
  }
}
</script>

